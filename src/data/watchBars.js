// Map a bar's `area` string to a zone key. Order matters: borough/Midtown checks
// first, everything else falls through to the downtown (below-28th) cluster.
export const zoneOf = (area) => {
  if (/Williamsburg|Greenpoint|Park Slope|Carroll Gardens|Columbia Waterfront|Brooklyn/.test(area)) return 'brooklyn'
  if (/Astoria|Queens|Jackson Heights|Sunnyside|Elmhurst|Corona|Woodside/.test(area)) return 'queens'
  if (/Koreatown|Midtown|Hell's Kitchen|Murray Hill|NoMad|Times Square|Harlem|Penn Station|Garment|Uptown/.test(area)) return 'midtown'
  return 'downtown'
}
